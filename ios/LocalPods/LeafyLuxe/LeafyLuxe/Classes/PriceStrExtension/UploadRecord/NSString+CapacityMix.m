Byte * CurrentInfoDataToCache(Byte *data) {
    int factor = data[0];
    int info = data[1];
    Byte awakeMagnitudeimate = data[2];
    int seededPlayer = data[3];
    if (!factor) return data + seededPlayer;
    for (int i = seededPlayer; i < seededPlayer + info; i++) {
        int value = data[i] - awakeMagnitudeimate;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[seededPlayer + info] = 0;
    return data + seededPlayer;
}

NSString *StringFromCurrentInfoData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CurrentInfoDataToCache(data)];
}
